# 1. Create and Access List Elements
my_list = [10, 5, 30, 20, 15]
print("Original List:", my_list)

# Access elements
print("First element:", my_list[0])
print("Third element:", my_list[2])

# 2. Add and Remove List Elements

# Add element
my_list.append(25)
print("After adding element:", my_list)

# Remove element
my_list.remove(5)
print("After removing element:", my_list)

# 3. Sort Elements
my_list.sort()
print("Sorted List:", my_list)

# 4. Reverse Elements
my_list.reverse()
print("Reversed List:", my_list)